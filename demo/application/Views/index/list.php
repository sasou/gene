    </fieldset>
  <blockquote class="layui-elem-quote">
* 优雅：优雅微架构，提供松耦合的、有一定的有界上下文的面向服务架构，按需组合，适应DDD领域驱动设计；</br>
* 极速：运行速度最快的框架：Nginx+PHP-FPM模式、swoole模式下均是最快，没有之一；</br>
* 灵活：支持传统MVC模式；同时支持MVCS模式：瘦MC模式，通过增加service层更好的实现模块封装；</br>
* 简单：一分钟demo入门，优雅而简单；</br>
* 场景：胜任WEB应用、微服务、常驻进程等场景；</br>
* 路由：完整支持HTTP REST请求；底层采用二叉树查找算法，性能强劲；</br>
* 钩子：路由支持全局前置、后置钩子；支持自定义前置钩子；</br>
* 依赖注入：参考 Java Spring 的 Bean 设计思想，实现了简易好用的IoC；支持全局注入，局部控制反转等；</br>
* 中间件：AOP (面向切面编程)，配置文件注册对象，调用方便且解耦；</br>
* 工厂：强大的工厂单例支持；</br>
* 数据库：封装高性能PDO ORM类库，支持mysql、mariadb、mssql、postgresql、sqlite数据库；</br>
* 配置：配置缓存到进程，自动更新；</br>
* 验证：万能验证类，内置常用基础验证规则，支持自定义规则，灵活强大；</br>
* 视图：方式一、使用编译模板引擎，支持模板标签，支持模板缓存；方式二、使用原生PHP做模板引擎；两种方式均支持布局、属性赋值、对象引用等；</br>
* 长连接：按进程保持的长连接，支持Mysql/Redis/Memcached；持久连接断开自动检测</br>
* 命令行：封装了命令行开发基础设施，可快速开发控制台程序、守护进程；</br>
* 缓存：支持两种缓存，一是方法级定时缓存；二是实时版本缓存（创新功能：高效的实时缓存方案，轻松解决复杂缓存的更新，比如分页数据的缓存）； </br>
* 自动加载：基于 PSR-4，支持自定义扩展类库加载目录；</br>
* 完美支持Swoole（gene框架严格测试，常驻进程下低内存占用，无内存泄露）；</br>
* 其他：redis、memcached类库二次封装；</br>
  </blockquote>
